    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        boolean CardNoCheck = false;
        boolean CardNameCheck = false;
        boolean BillingAddressCheck = false;
        boolean CardDateCheck = false;
        boolean CVCCheck = false;
        
        if (CardNoInput.getText().equals("")) {
            CardNoCheck = false;
            NotificationText.setText("Please insert your card number.");
        }
        
        else {
            CardNoCheck = true;
        }
        
        if (CardNoCheck == true && chNameInput.getText().equals("")) {
            CardNameCheck = false;
            NotificationText.setText("Please input the cardholder's name.");
        }
        
        else if (CardNoCheck == true) {
            CardNameCheck = true;
        }
        
        if (CardNameCheck == true && bAddressInput.getText().equals("")) {
            BillingAddressCheck = false;
            NotificationText.setText("Please insert the billing address");
        }
        
        else if (CardNameCheck == true) {
            BillingAddressCheck = true;
        }
        
        if (BillingAddressCheck == true && DateInput.getText().equals("")) {
            CardDateCheck = false;
            NotificationText.setText("Please insert the expiry date of your card.");
        }
        
        else if (BillingAddressCheck == true) {
            CardDateCheck = true;
        }
        
        if (CardDateCheck == true && CVCInput.getText().equals("")) {
            CVCCheck = false;
            NotificationText.setText("Please insert your card's CVC number.");
        }
        
        else if (CardDateCheck == true) {
            CVCCheck = true;
        }
        
        if (CVCCheck == true) {
            CustomerReceipt cReceipt = new CustomerReceipt();
            cReceipt.setVisible(true);
            cReceipt.pack();
            cReceipt.setLocationRelativeTo(null);   
            this.dispose();
        }
    }//GEN-LAST:event_jButton2MouseClicked

